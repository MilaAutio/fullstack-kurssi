//total likes
describe('total likes', () => {

  test('when list has only one blog equals the likes of that', () => {
    const result = listHelper.totalLikes(listWithOneBlog)
    expect(result).toBe(5)
  })

  test('when list has multiple blog equals the likes of that', () => {
    const result = listHelper.totalLikes(listWithMultipleBlogs)
    expect(result).toBe(36)
  })

  test('when list has zero blog equals the likes of that', () => {
    const result = listHelper.totalLikes(listWithZeroBlog)
    expect(result).toBe(0)
  })

  test('when list has blog without likes equals the likes of that', () => {
    const result = listHelper.totalLikes(listWithoutLikes)
    expect(result).toBe(2)
  })

  test('when list has blog with wrong type likes', () => {
    const result = listHelper.totalLikes(listWithWrongTypeLikes)
    expect(result).toBe(3)
  })

  test('when list has not any likes', () => {
    const result = listHelper.totalLikes(listWithoutAnyLikes)
    expect(result).toBe(0)
  })

})

